public class Calculadora {

    public static int soma(int x, int y){
        int soma = x + y;
        return soma;
    }
    public static int mult(int x, int y){
        int mult = x * y;
        return mult;
    }
    public static int div(int x, int y){
        int div = x / y;
        return div;
    }
    public static int sub(int x, int y){
        int sub = x - y;
        return sub;
    }

}
